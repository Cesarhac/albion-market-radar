import type { AlbionCity, CityPrice, Item } from '@/types/albion';

const minutesAgo = (minutes: number) => new Date(Date.now() - minutes * 60_000).toISOString();

const prices = (
  values: Array<[AlbionCity, number, number, number, number, number]>,
): CityPrice[] =>
  values.map(([city, sellPriceMin, buyPriceMax, averagePrice, estimatedVolume, ageMinutes]) => ({
    city,
    sellPriceMin,
    buyPriceMax,
    averagePrice,
    estimatedVolume,
    updatedAt: minutesAgo(ageMinutes),
    updateStatus: ageMinutes <= 120 ? 'updated' : ageMinutes <= 720 ? 'medium' : 'outdated',
    hasMarketData: sellPriceMin > 0 || buyPriceMax > 0,
  }));

